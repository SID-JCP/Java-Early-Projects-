public class Variables {
    public static int tool = 0;
    public static int LineShape = 1;
    public static int pattern = 3;
    public static int shape = 0;

    public int getTool() {
        return tool;
    }

    public int getLineShape() {
        return LineShape;
    }

    public int getPattern() {
        return pattern;
    }

    public int getShape() {
        return shape;
    }


    public void setTool(int tool) {
        Variables.tool = tool;
    }

    public void setLineShape(int lineShape) {
        LineShape = lineShape;
    }

    public void setPattern(int pattern) {
        Variables.pattern = pattern;
    }

    public void setShape(int shape) {
        Variables.shape = shape;
    }


}
